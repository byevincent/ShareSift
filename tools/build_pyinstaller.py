"""v0.46 step 2: build a minimal-footprint ShareSift binary via
PyInstaller.

Earlier attempts (v0.38) produced a 1.5GB binary because PyInstaller
auto-collected sklearn's whole tree (200+ submodules), pandas (not
even imported!), and lots of test fixtures. This spec aggressively
trims:

- **Excludes ML training stack entirely** — torch, transformers,
  peft, bitsandbytes, accelerate, datasets, trl, unsloth,
  llama_cpp, kingfisher. These are content-inference and
  content-training only; operators using ``score-paths`` don't
  touch them.
- **Excludes SMB/impacket** — operators using local file lists
  don't need them; SMB-direct operators install via pipx with
  the [smb] extra which adds smbprotocol natively.
- **Excludes pandas, matplotlib, seaborn** — never imported by
  Stage 1 cascade or rule engine, but PyInstaller's hooks pull
  them via sklearn / scipy / lightgbm transitively.
- **Trims sklearn**: includes only ``sklearn.base``,
  ``sklearn.utils``, ``sklearn.tree``, ``sklearn.linear_model``,
  ``sklearn.calibration`` (used by isotonic / beta calibrators)
  + dependencies.
- **Trims numpy/scipy** to runtime-only — no testing fixtures.
- **--onefile** since the binary is intended to drop on a Kali
  box without a separate install dir.

Result target: < 200 MB single binary covering ``score-paths``,
``scan-files`` (rule + extractor — but no content classifier),
``to-snaffler-tsv``, ``query``, ``export``, ``sort``.

Operators wanting SMB-direct, network discovery, or content
classifier use ``pipx install 'sharesift[smb,network-enum,content-inference]'``
instead.

Build:

    uv run python tools/build_pyinstaller.py
    # Output: dist/sharesift (Linux), dist/sharesift.exe (Windows)
"""

