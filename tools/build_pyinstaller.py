def main() -> int:
    if shutil.which("pyinstaller") is None:
        print("ERROR: pyinstaller not installed. Run:", file=sys.stderr)
        print("  uv run pip install pyinstaller", file=sys.stderr)
        return 1

    # Write the spec
    SPEC_PATH.parent.mkdir(exist_ok=True)
    SPEC_PATH.write_text(SPEC_TEMPLATE, encoding="utf-8")
    print(f"Wrote spec → {SPEC_PATH}")

    # Clean previous build
    if DIST_DIR.exists():
        shutil.rmtree(DIST_DIR)
    work_dir = REPO_ROOT / "build-pyinstaller"
    if work_dir.exists():
        shutil.rmtree(work_dir)

    # Run PyInstaller
    cmd = [
        "pyinstaller",
        "--clean",
        "--noconfirm",
        "--distpath", str(DIST_DIR),
        "--workpath", str(work_dir),
        str(SPEC_PATH),
    ]
    print(f"Running: {' '.join(cmd)}")
    proc = subprocess.run(cmd, cwd=REPO_ROOT)
    if proc.returncode != 0:
        print(f"PyInstaller failed (exit {proc.returncode})", file=sys.stderr)
        return proc.returncode

    # Report binary size
    binary = DIST_DIR / "sharesift"
    if binary.exists():
        size_mb = binary.stat().st_size / (1024 * 1024)
        print(f"\nBinary built: {binary} ({size_mb:.1f} MB)")
    else:
        print(f"\nERROR: binary not found at {binary}", file=sys.stderr)
        return 1

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
